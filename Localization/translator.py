def MakeTemplate(dic,template,locs,warn):
  #Sort the IPA keys, but leave the non-IPA keys intact
  sorted = [key for key in dic.keys() if key.startswith('U+')]
  sorted.sort()
  sorted.extend([key for key in dic.keys() if not key.startswith('U+')])
  nwarnings = 0
  fh = open(template, 'r')
  data = fh.read().decode('utf-8')
  fh.close()
  ctmpl = None
  vtmpl = None
  data = re.sub(r'\s*#.+', '', data)
  data = re.sub(r'\n+$', '', data)
  code = re.match(r'(.+?)\..*', template).group(1)
  #print "Code is " + code
  lines = re.split(r'\n', data)
  trans = dict()
  for line in lines:
    #print line
    [english,foreign] = line.split(':')
    if english == 'CTemplate': ctmpl = foreign
    elif english == 'VTemplate': vtmpl = foreign
    else: trans[english.lower()] = foreign.lower()
  for ipa in sorted:
    locs = dic[ipa]
    english = locs['en'].lower()
    ewords = re.split(r'\s+', english)
    fwords = list()
    full = True
    i = 0
    while (True):
      if not full: break
      if i >= len(ewords): break
      gotit = False
      w0 = ewords[i]
      if i < len(ewords)-1:
        w1 = ewords[i+1]
        #print "Looking for '%s %s' in %s" % (w0,w1,len(trans))
        if trans.get(w0 + ' ' + w1) is not None:
          fwords.append(trans[w0 + ' ' + w1])
          i += 1
          gotit = True
          #print "got it"
      if (not gotit) and trans.get(w0) is not None:
          fwords.append(trans[w0])
          gotit = True
      if (not gotit): full = False
      i += 1
    if full and len(fwords):
      if english.endswith('vowel'): fwords = Rearrange(fwords,vtmpl)
      else: fwords = Rearrange(fwords,ctmpl)
      fword = ' '.join(fwords)
      fword = fword[0].upper() + fword[1:]
      #print "%s => %s" % (english, fword)
      dic[ipa][code] = fword
    else:
      #print "Warning: could not find translation for %s" % (english)
      dic[ipa][code] = ''
  return dic

def Rearrange(words,template):
  template = [int(x) for x in template if int(x) <= len(words)]
  newwords = [w for w in words]
  for i in range(0,len(words)):
    #print  template
    #print words
    #print i
    #print newwords
    newwords[i] = words[template[i]-1]
  return newwords

