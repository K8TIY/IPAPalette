def ReformatDic(dic):
  secs = list()
  # Sort the IPA keys, but leave the non-IPA keys intact
  sorted = [key for key in dic.keys() if key.startswith('U+')]
  sorted.sort()
  extended = [key for key in dic.keys() if not key.startswith('U+')]
  extended.sort()
  sorted.extend(extended)
  for ipa in sorted:
    locs = dic[ipa]
    trans = ['loc_%s: %s' % (lang,locs[lang]) for lang in locs.keys()]
    trans.sort()
    secs.append('Glyph: %s\n' % (ipa) + '\n'.join(trans))
  return '\n%%\n'.join(secs)

